WITH FRONT AS (
    SELECT SUM(CODE) CODE
    FROM SKILLCODES 
    WHERE CATEGORY = 'Front End' 
),

T AS (
    SELECT 
        CASE
    WHEN SKILL_CODE & FRONT.CODE > 0 AND SKILL_CODE & (SELECT CODE FROM SKILLCODES WHERE NAME = 'Python') > 0 THEN 'A' 
    WHEN SKILL_CODE & (SELECT CODE FROM SKILLCODES WHERE NAME = 'C#') > 0 THEN 'B' 
    WHEN SKILL_CODE & FRONT.CODE > 0 THEN 'C' 
    END AS GRADE,
    ID, EMAIL 
    FROM DEVELOPERS D
    JOIN FRONT 
    ORDER BY GRADE, ID 
)

SELECT 
    * 
FROM T 
WHERE GRADE IS NOT NULL